# abs()
number = abs(56.7)
print(number)

print(abs(-120))

# min()

number = min(23, 5, 60, 120, -6)
print(number)

# max()

number_1 = max(23, 5, 60, 120, -6)
print(number_1)

print(5 ** 3)
print(pow(5, 3))


# round()

print(round(3.2562115))
print(round(3.2562115, 5))
print(round(3.45655, 2))

print(pow(25, 0.5))
print(25 ** 0.5)
